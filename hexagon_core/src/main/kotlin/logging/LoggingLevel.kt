 * - [ERROR][LoggingLevel.ERROR].
 * - [OFF][LoggingLevel.OFF].
 */
enum class LoggingLevel {
    TRACE,
    DEBUG,
    INFO,
    WARN,
    ERROR,
    OFF,
}
